// Declaring my age
var myAge = 26;
// Declaring early years 
var earlyYears = 2;
earlyYears *= 10.5;
// Declaring later years
laterYears = myAge -= 2;
// Multiplying by 4 and assigning the new value
laterYears *= 4;
// This result will give my age in dog years
myAgeInDogYears = earlyYears + laterYears;
// putting my name to all lower case
var myName = 'Allie'.toLowerCase();
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`);
